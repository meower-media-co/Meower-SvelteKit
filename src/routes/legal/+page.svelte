	<p>
		We collect analytics to be able to improve our services. We strive to make
		sure that analytics information is not personally identifying and cannot be
		abused. We have implemented multiple measures to protect your information,
		including disassociating information from individual users and IPs, and
		removing any information that can allow for a specific user to be
		identified.
	</p>
	<p>We take the following measures to prevent misuse of analytics:</p>
	<ul>
		<li>
			Only allow select members of the team to have access to raw analytics
			data.
		</li>
		<li>
			Anonymize all analytics data by stripping user identifiers and IP
			addresses.
		</li>
		<li>Send a reduced amount of information.</li>
		<li>
			If the data is too sensitive, run the data through algorithms to prevent
			any single user&#39;s information being singled out.
		</li>
	</ul>
